func (app *application) userSignupPost(w http.ResponseWriter, r *http.Request) {
	var form userSignupForm

	err := app.decodePostForm(r, &form)
	if err != nil {
		app.clientError(w, http.StatusBadRequest)
		return
	}

	form.CheckField(validator.NotBlank(form.Name), "name", "name cannot be empty. duh")
	form.CheckField(validator.NotBlank(form.Email), "email", "email cant be empty dumbass")
	form.CheckField(validator.Matches(form.Email, validator.EmailRX), "email", "invalid email bruh")
	form.CheckField(validator.NotBlank(form.Password), "password", "wtf empty password?")
	form.CheckField(validator.MinChars(form.Password, 8), "password", "password must be atleast 8 chars foo")

	if !form.Valid() {
		data := app.newTemplateData(r)
		data.Form = form
		app.render(w, r, http.StatusUnprocessableEntity, "signup.tmpl", data)
		return
	}

	err = app.users.Insert(form.Name, form.Email, form.Password)
	if err != nil {
		if errors.Is(err, models.ErrDuplicateEmail) {
			form.AddFieldError("email", "email already exists dude. too bad")

			data := app.newTemplateData(r)
			data.Form = form
			app.render(w, r, http.StatusUnprocessableEntity, "signup.tmpl", data)
		} else {
			app.serverError(w, r, err)
		}
		return
	}

	app.sessionManager.Put(r.Context(), "flash", "your signup was legit bro")

	http.Redirect(w, r, "/user/login", http.StatusSeeOther)
}

